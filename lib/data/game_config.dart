class GameConfig {

  // ── عدد الأعمدة والصفوف في الشبكة ──
  // زيادة COLS  = شبكة أضيق (خلايا أكثر أفقياً)
  // زيادة ROWS  = شبكة أطول  (خلايا أكثر رأسياً)
  static const int COLS = 9;
  static const int ROWS = 13;

  // ── إزاحة الخريطة عن حواف الشاشة ──
  // 0.0 = محاذاة تلقائية بالمنتصف
  // قيمة موجبة = يتحرك يميناً / للأسفل
  // قيمة سالبة = يتحرك يساراً / للأعلى
  static const double extraOffsetX = 0.0;
  static const double extraOffsetY = 0.0;

  // ── حجم الخلية يدوياً (0 = تلقائي) ──
  // مثال: 40.0 تجعل كل خلية 40×40 بكسل
  // اتركها 0 لحساب تلقائي
  static const double forceCellSize = 0.0;

  // ── منطقة حظر البناء حول المسار ──
  // 1 = يحظر الخلايا التي تمر فيها الطريق فقط
  // 2 = يحظر الخلايا المجاورة أيضاً (أوسع)
  static const int pathBlockPadding = 1;

  // ── موضع بداية المسار ──
  // الصف الذي يبدأ منه المسار (0 = الأعلى)
  static const int pathStartRow = 0;
}
